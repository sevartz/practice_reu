SYSTEM = (
    "Ты — дружелюбный помощник-аналитик покупок в продуктовом магазине. "
    "Обращайся к клиенту на «вы», пиши тепло и по делу, кратко (5–7 предложений). "
    "Опирайся только на данные, которые тебе дали, ничего не выдумывай."
)


def anchors_prompt(anchors_text: str, reorder_share: int) -> str:
    """Сервис 1: прокомментировать «якорные» товары клиента."""
    return (
        "Вот «якорные» товары клиента — то, что он покупает снова и снова "
        "(в скобках: сколько раз брал и какая доля повторных покупок):\n"
        f"{anchors_text}\n\n"
        f"В среднем доля повторных покупок у клиента — около {reorder_share}%. "
        "Опишите тёпло и по-человечески, что это за покупатель: к чему он "
        "привязан, насколько предсказуемы его покупки. Затем дайте 1–2 совета — "
        "например, что стоит добавить в подписку/автозаказ, чтобы не забывать любимое."
    )


def rhythm_prompt(avg_days: float, top_day_human: str, top_hour: int) -> str:
    """Сервис 2: подсказать, когда клиенту удобнее сделать следующий заказ."""
    return (
        f"Клиент делает новый заказ в среднем раз в {avg_days:.0f} дней. "
        f"Чаще всего он заказывает в {top_day_human}, около {top_hour}:00.\n\n"
        "Подскажите тёпло и коротко: когда стоит ждать его следующего заказа "
        "и в какое время суток удобнее оформлять доставку. "
        "Добавьте 1 дружеский совет, как не пропустить привычный ритм покупок."
    )


def cart_prompt(must_have_text: str, impulse_text: str) -> str:
    """Сервис 3: прокомментировать приоритеты и импульсные покупки."""
    return (
        "Товары, которые клиент почти всегда кладёт в корзину ПЕРВЫМИ "
        "(его приоритеты, must-have):\n"
        f"{must_have_text}\n\n"
        "Товары, которые он обычно добавляет в самом конце "
        "(похоже на импульсные покупки):\n"
        f"{impulse_text}\n\n"
        "Опишите тёпло, что для клиента главное в корзине, а что он берёт «под настроение». "
        "Дайте 1–2 мягких совета, как планировать покупки и не переплачивать за импульсное."
    )
