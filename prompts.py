def anchors_prompt(anchors_text: str, reorder_share: int) -> str:
    return (
        "Якорные товары клиента (в скобках: сколько раз брал, "
        "доля повторных покупок):\n"
        f"{anchors_text}\n\n"
        f"Средняя доля повторных покупок — около {reorder_share}%.\n\n"
        "Задача:\n"
        "1) Одним абзацем опишите портрет покупателя: к чему он привязан, "
        "насколько предсказуем.\n"
        "2) Вторым абзацем предложите включить автозаказ или подписку "
        "на 1–2 КОНКРЕТНЫХ товара из списка выше (назовите их) — "
        "объясните, почему именно эти.\n\n"
        "Пиши сплошным текстом, без списков и Markdown."
    )


def rhythm_prompt(avg_days: float, top_day_human: str, top_hour: int) -> str:
    return (
        f"Клиент оформляет новый заказ примерно раз в {avg_days:.0f} дней. "
        f"Чаще всего — в {top_day_human}, около {top_hour}:00.\n\n"
        "Задача:\n"
        "1) Одним абзацем скажите, когда ждать следующий заказ "
        f"(посчитайте: последний заказ + {avg_days:.0f} дней) и почему "
        f"именно {top_day_human} в {top_hour}:00 — удачное окно.\n"
        "2) Вторым абзацем предложите поставить напоминание на конкретный "
        f"день недели ({top_day_human}) и время (за 1–2 часа до {top_hour}:00), "
        "чтобы не выпадать из привычного ритма.\n\n"
        "Пиши сплошным текстом, без списков и Markdown."
    )


def cart_prompt(must_have_text: str, impulse_text: str) -> str:
    return (
        "Товары, которые клиент кладёт в корзину ПЕРВЫМИ (must-have):\n"
        f"{must_have_text}\n\n"
        "Товары, которые он добавляет В КОНЦЕ (похоже на импульс):\n"
        f"{impulse_text}\n\n"
        "Задача:\n"
        "1) Одним абзацем опишите, что для клиента главное в корзине "
        "(сошлитесь на конкретные must-have выше), а что он берёт "
        "«под настроение» (сошлитесь на конкретные импульсные выше).\n"
        "2) Вторым абзацем предложите либо добавить конкретный must-have "
        "в подписку, либо включить лимит на импульсные категории — "
        "назовите категорию или товар из списков выше.\n\n"
        "Пиши сплошным текстом, без списков и Markdown."
    )